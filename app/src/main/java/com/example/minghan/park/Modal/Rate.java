public class Rate {
    public double firstHour;
    public double nextHour;
    public Rate(){}

    public double getFirstHour() {
        return firstHour;
    }

    public void setFirstHour(double firstHour) {
        this.firstHour = firstHour;
    }

    public double getNextHour() {
        return nextHour;
    }

    public void setNextHour(double nextHour) {
        this.nextHour = nextHour;
    }
}
